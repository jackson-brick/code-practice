def loading_sequence(seconds_to_load):
    """
    Displays a loading sequence for a preset number of loading cycles
    Not chosen by user, predisposed by me
    """
    loadingList = ["fun paragraphs" , "challenging words" , "all the code" , "security measures" , "eye-tracking software" , "the CIA's confidential files" , "your address" , "the shopping list on the fridge" , "all the answers to the algebra test" , "flashcards" , "funny quips"]
    usedLoadingList = []
    for lcv in range(seconds_to_load):
        usedLoadingList.append(random.choice(loadingList))
        loadingList.remove(f"{usedLoadingList[lcv]}")
    os.system('clear')
    time.sleep(0.5)
    while seconds_to_load > 0:
        print(f"Loading {usedLoadingList[(seconds_to_load - 1)]}".center(z))
        time.sleep(0.5)
        os.system('clear')
        print(f"Loading {usedLoadingList[(seconds_to_load - 1)]}  .".center(z))
        time.sleep(0.5)
        os.system('clear')
        print(f"Loading {usedLoadingList[(seconds_to_load - 1)]}  .  .".center(z))
        time.sleep(0.5)
        os.system('clear')
        print(f"Loading {usedLoadingList[(seconds_to_load - 1)]}  .  .  .".center(z))
        time.sleep(1)
        os.system('clear')
        seconds_to_load -= 1
    while len(usedLoadingList) > 0:
        loadingList.append(usedLoadingList[-1])
        usedLoadingList.pop(-1)

